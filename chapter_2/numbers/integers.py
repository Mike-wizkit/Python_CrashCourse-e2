# you can add (+), subtract (-), multiply (*), and divide (/) integers in python
print(2 + 3)

print(3 - 2)

print(2 * 3)

print(3 / 2)

# in a terminal session, Python simply returns the result of the operation
# Pyton uses two multiplication symbols to represent exponents:
print(3 ** 2)

print(3 ** 3)

print(10 ** 6)

# Python supports the order of operations to,
# so you can use multiple operations in one expression
print(2 + 3*4)

