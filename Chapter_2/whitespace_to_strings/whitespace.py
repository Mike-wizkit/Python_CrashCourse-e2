# Normal print
print("Python")

# To add a tab to your text use the character combination: \t
# like below
print("\tPython")

# To add a new line in a sting, use the character combination: \n
# like below
print("Languages:\nPython\nC\nJavaScript")

# You can also combine tabs and newlines in a single string.
# the string "\n\t" tells Python to move to a new line, and start
# the next line with a tab (example below)
print("Languages:\n\tPyhton\n\tC\n\t\JavaScript")